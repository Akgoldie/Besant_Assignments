letter=input("Enter the letter :")

if(letter=='e' or letter=='E'):
    print("Excellent")
elif(letter=='v' or letter=='V'):
    print("Very Good")
elif(letter=='g' or letter=='G'):
    print("Good")
elif(letter=='a' or letter=='A'):
    print("Average")
elif(letter=='f' or letter=='F'):
    print("Fail")

else:
    print("Invalid character")